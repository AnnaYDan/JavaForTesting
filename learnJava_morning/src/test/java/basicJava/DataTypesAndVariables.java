package basicJava;

public class DataTypesAndVariables {

	public static void main(String[] args) {
		// this program is to learn data types and variables
		// how to store you data in variables
		
		// string variable
		String name = "Java";
		// integer variable
		int age = 21;
		// double variable
		double index = 5.6;
		// char variable
		char firstLetter ='J';
		
		// printing with concatenation strings and variables
		System.out.println("My name is  " + name );
		System.out.println("My age is " + age);
		System.out.println("My hight is " + index);
		System.out.println("My gender is " + firstLetter);
	
	}
	
}
