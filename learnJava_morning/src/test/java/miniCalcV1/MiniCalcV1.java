package miniCalcV1;

public class MiniCalcV1 {

	public static void main(String[] args) {

		int a = 10;
		int b = 2;
		
		int sum = a + b;
		System.out.println(sum);

		int sub = a - b;
		System.out.println(sub);
		
		int mult = a * b;
		System.out.println(mult);
		
		int div = a / b;
		System.out.println(div);
		
	}

}
