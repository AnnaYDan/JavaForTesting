package miniCalcV3;

public class miniCalcMethods {

	public static int add(int a, int b) {
		int result = a + b;
		return result;
	}
	
	public static int sub(int a, int b) {
		int result = a - b;
		return result;
	}

	public static int mult(int a, int b) {
		int result = a * b;
		return result;
	}
	
	public static int div(int a, int b) {
		int result = a * b;
		return result;
	}
	
}
